import java.util.Arrays;

public class Lista {
    public static void imprimirLista(Integer[] list) {
        System.out.println(Arrays.toString(list));
    }

    public static void imprimirLista(Double[] list) {
        System.out.println(Arrays.toString(list));
    }

    public static void imprimirLista(Character[] list) {
        System.out.println(Arrays.toString(list));
    }
}
